package datentypen.char_;

public class Char {

	public static void main(String[] args) {
		
		char a = '\u0025'; // %
		System.out.println(a);
		char b = '\u0040'; // @
		System.out.println(b);
		char c = '\u2192'; // ->
		System.out.println(c); 
		char d = 'd';
		System.out.println(d);
		char e = 1623;
		char f = 1524;
		System.out.println(e * f);

	}

}
